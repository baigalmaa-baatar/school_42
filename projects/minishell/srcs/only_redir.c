#include "../inc/minishell.h"

bool	only_redir(char *s)
{
	size_t	size;

	size = ft_strlen(s);
	if ((s[size - 1] == '<' || s[size - 1] == '>') && s[size] == '\0')
		return (true);
	return (false);
}
